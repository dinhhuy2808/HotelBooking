package com.hotelbooking.springmvcsecurity.model;

public class Resdetail {
	int resdetailid;
	int startdate;
	int enddate;
	int numberofroom;
	int numberofguest;
	int numberofnight;
	String position;
	public int getResdetailid() {
		return resdetailid;
	}
	public void setResdetailid(int resdetailid) {
		this.resdetailid = resdetailid;
	}
	public int getStartdate() {
		return startdate;
	}
	public void setStartdate(int startdate) {
		this.startdate = startdate;
	}
	public int getEnddate() {
		return enddate;
	}
	public void setEnddate(int enddate) {
		this.enddate = enddate;
	}
	public int getNumberofroom() {
		return numberofroom;
	}
	public void setNumberofroom(int numberofroom) {
		this.numberofroom = numberofroom;
	}
	public int getNumberofguest() {
		return numberofguest;
	}
	public void setNumberofguest(int numberofguest) {
		this.numberofguest = numberofguest;
	}
	public int getNumberofnight() {
		return numberofnight;
	}
	public void setNumberofnight(int numberofnight) {
		this.numberofnight = numberofnight;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	
	
}
